use crate::adder::adder;

pub fn multiplier(a: u32, b: u32) -> u32 {
    let mut i = 0;
    let mut r = 0;

    while i < 32 {
        if b & (1 << i) != 0 {
            r = adder(r, a << i);
        }
        i = i + 1;
    }
    r
}
